import java.util.*;
class countn{
    public static void main(String[] args)
    {
        Scanner sc=new Scanner(System.in);
        int a=sc.nextInt();
        int c=0;
        for(int i=0;i<=a;i++)
        {
            c=c+i;
        }
        if(c!=0)
        {
            System.out.print(c);
        }
    }
}
